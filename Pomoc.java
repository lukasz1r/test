/*
 * <h3>Obiekt pomocniczy pozwalający pezechować dwie wartości naraz - do wyboru odpowiedniego rodzaju użytkownika i jego indeksu na liście</h3>
 */

public class Pomoc {
    private int x, y;
    Pomoc(int x, int y){
        this.x=x;
        this.y=y;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
}
    